package dataTypes;

// There are basically 2 types of data types
// Primitive & Derived
// Primitive contains int, double, float, char, boolean, byte, short etc...
// Derived contains Array, ArrayList, HashMap, String etc...

public class DataTypes {

    public static void main(String[] args) {

        int a = 10;
        double d = 20.5;
        char c = 'H';
        boolean b = true; // Boolean accepts only 2 values, i. e, true or false

        String s = "Welcome"; // String is inbuilt class in java, also used as data type
        // String is collection of characters, should be included in double quotes

        System.out.println(a); // Prints value of a
        System.out.println(d);
        System.out.println(c);
        System.out.println(b);
        System.out.println(s);
    }
}
